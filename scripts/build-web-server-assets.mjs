import { mkdir, readFile, writeFile, copyFile } from 'node:fs/promises';
import { dirname, join, parse } from 'node:path';
import { builtinModules } from 'node:module';

const root = process.cwd();
const outputRoot = join(root, 'server-dist');
const bridgeFile = join(
  outputRoot,
  'ts/web/provisioning/WebProvisioningBridge.node.mjs'
);
const rootPackagePath = join(root, 'package.json');

const builtinModuleIds = new Set(
  builtinModules.flatMap(name => [name, `node:${name}`])
);

function getPackageName(moduleId) {
  if (
    builtinModuleIds.has(moduleId) ||
    moduleId.startsWith('.') ||
    moduleId.startsWith('/') ||
    moduleId.startsWith('\0')
  ) {
    return undefined;
  }

  if (moduleId.startsWith('@')) {
    const [scope, name] = moduleId.split('/');
    if (!scope || !name) {
      return undefined;
    }

    return `${scope}/${name}`;
  }

  return moduleId.split('/')[0];
}

function getExternalPackageNames(code) {
  const packages = new Set();
  const patterns = [
    /\bimport(?:[^'"()]+?from)?["']([^"']+)["']/g,
    /\bexport[^'"()]+?from["']([^"']+)["']/g,
    /\bimport\s*\(\s*["']([^"']+)["']\s*\)/g,
  ];

  for (const pattern of patterns) {
    let match;
    while ((match = pattern.exec(code)) != null) {
      const packageName = getPackageName(match[1]);
      if (packageName) {
        packages.add(packageName);
      }
    }
  }

  return [...packages].sort((left, right) => left.localeCompare(right));
}

async function copyProjectFile(relativePath) {
  const source = join(root, relativePath);
  const destination = join(outputRoot, relativePath);
  await mkdir(dirname(destination), { recursive: true });
  await copyFile(source, destination);
}

const bridgeCode = await readFile(bridgeFile, 'utf8');
const rootPackage = JSON.parse(await readFile(rootPackagePath, 'utf8'));
const externalPackageNames = getExternalPackageNames(bridgeCode);
const dependencies = {};

for (const packageName of externalPackageNames) {
  const version =
    rootPackage.dependencies?.[packageName] ??
    rootPackage.devDependencies?.[packageName];
  if (!version) {
    throw new Error(
      `Missing dependency version for ${packageName} in ${rootPackagePath}`
    );
  }
  if (version.startsWith('workspace:')) {
    throw new Error(
      `Server bundle imports workspace dependency ${packageName}; bundle or publish it before creating a deployable artifact`
    );
  }

  dependencies[packageName] = version;
}

const serverPackage = {
  name: `${rootPackage.name}-web-bridge`,
  version: rootPackage.version,
  private: true,
  type: 'module',
  engines: rootPackage.engines,
  packageManager: rootPackage.packageManager,
  scripts: {
    start: 'node ts/web/provisioning/WebProvisioningBridge.node.mjs',
  },
  dependencies,
};

await mkdir(outputRoot, { recursive: true });
await writeFile(
  join(outputRoot, 'package.json'),
  `${JSON.stringify(serverPackage, null, 2)}\n`
);

await copyProjectFile('config/production.json');
await copyProjectFile('build/jumbomoji.json');
await copyProjectFile('build/optional-resources.json');
await copyProjectFile('deploy/web/env.production');

const readme = `# Signal Web Bridge Server Artifact

This directory is generated by \`pnpm run web:build:server\`.

## Install runtime dependencies

\`\`\`bash
pnpm install --prod
\`\`\`

## Start

\`\`\`bash
set -a
. deploy/web/env.production
set +a
pnpm start
\`\`\`

The bridge entry is:

\`\`\`text
ts/web/provisioning/WebProvisioningBridge.node.mjs
\`\`\`
`;

await writeFile(join(outputRoot, 'README.md'), readme);

console.log(
  `Built server artifact at ${parse(outputRoot).base} with ${externalPackageNames.length} external runtime dependencies.`
);
